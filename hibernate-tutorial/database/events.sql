CREATE TABLE HR.EVENTS  (	
 EVENT_ID NUMBER(6,0), 
 EVENT_DATE DATE, 
 EVENT_TITLE VARCHAR2(256), 
 CONSTRAINT EVENT_ID_PK PRIMARY KEY (EVENT_ID)
  ) 
  
  CREATE SEQUENCE HR.events_seq;
  
